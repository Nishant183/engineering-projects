    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/login", "root", "root");
            Statement stmt = con.createStatement();
            String query = "Select * from log;";
            ResultSet rs = stmt.executeQuery(query);
            int flag = 0;
            while (rs.next()) {
                String name = rs.getString(1);
                String pass = rs.getString(2);
                String a = jTextField1.getText();
                String b = new String(jPasswordField1.getPassword());
                if (name.equals(a) && pass.equals(b)) {
                   //t.start();
                    MainWindow home =new MainWindow();
                    this.setVisible(false);
                    home.setVisible(true);
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    flag = 1;
                    break;
                }
            }
            if (flag == 0) {
                JOptionPane.showMessageDialog(null,"ENTER CORRECT USERNAME OR PASSWORD");
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error in Connectivity");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

